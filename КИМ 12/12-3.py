comm = {
    (' ', 0): (' ', -1, 1),
    (' ', 1): (' ', 1, 2),
    (' ', 3): (' ', 1, 4),
    (' ', 4): (' ', 2, 4),
    ('0', 1): ('0', -1, 1),
    ('0', 2): ('0', 1, 2),
    ('0', 3): ('0', 1, 3),
    ('0', 4): ('0', 1, 4),
    ('1', 1): ('1', -1, 1),
    ('1', 2): ('1', 1, 3),
    ('1', 3): ('0', 1, 4),
    ('1', 4): ('1', 1, 4),
}
def mt(s):
    s = list(' ' + s + ' ')
    q = 0
    i = -1
    while True:
        cmd = comm[(s[i], q)]
        s[i] = cmd[0]
        if cmd[1] == 2: break
        i += cmd[1]
        q = cmd[2]
    return ''.join(s)
x = bin(800)[2:]
print(int(mt(x), 2))